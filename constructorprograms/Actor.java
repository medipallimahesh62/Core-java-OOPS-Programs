class Actor 
{
	public String aName;
	public int aAge;
	public Actor(String aName,int aAge)
	{
		this.aName=aName;
		this.aAge=aAge;
	}
	public void actorinfo()
	{
		System.out.println("actor name is"+aName);
		System.out.println("actor age is"+aAge);
	}

	public static void main(String[] args) 
	{
		System.out.println("main starts");
		Actor a1=new Actor("super star mahesh babu",37);
		a1.actorinfo();
		System.out.println("main ends");

	}
}
